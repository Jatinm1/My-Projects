    private void getEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm =  (javax.swing.table.DefaultTableModel)table.getModel();
            
            int rc = dtm.getRowCount();
            while(rc--!=0){
                dtm.removeRow(0);
            }
            
            ResultSet rs = db.DbConnect.st.executeQuery("select * from category_info order by cID");
           
            while(rs.next()){
                
                
                Object o[] = {rs.getInt("cID"),rs.getString("category")};
                dtm.addRow(o);
                
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    

