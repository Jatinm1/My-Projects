    private void updateCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateCategoryActionPerformed

        try{
            int rowID = table.getSelectedRow();
            if(rowID!=-1){
                int id = (int)table.getValueAt(rowID, 0);
                String selectedcategory = (String)table.getValueAt(rowID, 1);
                String updatedCategory = JOptionPane.showInputDialog("<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">Enter new name for this Category</p></html>");
                try{
                    if (updatedCategory!= null){

                        db.DbConnect.st.executeUpdate("update category_info set category = '"+updatedCategory+"' where cID = '"+id+"' ");
                        db.DbConnect.st.executeUpdate("update spendings set category = '"+updatedCategory+"' where category =                   '"+selectedcategory+"' ");
                        Icon successicon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/successicon.png"));

                        JOptionPane.showMessageDialog(null,"<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">Category Updated Successfully</p></html>","Success", JOptionPane.INFORMATION_MESSAGE,successicon);
                    }
                    else{}
                    getEntries();
                }
                catch(SQLSyntaxErrorException ex){
            Icon erroricon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/errorIcon.png"));
            JOptionPane.showMessageDialog(null, "<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">You can't use ' or &#34; in Category Name </p></html>", "Warning", HEIGHT, erroricon);
        }
                catch(SQLIntegrityConstraintViolationException ex){

                    Icon erroricon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/errorIcon.png"));
                    JOptionPane.showMessageDialog(null, "<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">Category with same name already exists, Please choose a different Name.</p></html>", "Warning", HEIGHT, erroricon);
                }
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_updateCategoryActionPerformed

