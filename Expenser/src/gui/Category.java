    private void deleteCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCategoryActionPerformed
        int rowID = table.getSelectedRow();

        if(rowID!=-1){

            Icon binicon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/binIcon.png"));
            int r = JOptionPane.showConfirmDialog(null, "<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">Deleting this Category will DELETE ALL TRANSACTIONS associated with it, Do You Still want to Delete ? </p></html>" , "Deletion Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, binicon);

            if(r==JOptionPane.YES_OPTION){
                String category = (String)table.getValueAt(rowID, 1);
                try{
                    

                    db.DbConnect.st.executeUpdate("delete from category_info where category = '"+category+"'");
                    db.DbConnect.st.executeUpdate("delete from spendings where category = '"+category+"'");
                    javax.swing.table.DefaultTableModel dtm =  (javax.swing.table.DefaultTableModel)table.getModel();
                    javax.swing.table.DefaultTableModel dtmo =  (javax.swing.table.DefaultTableModel)ExpenseTracker.table.getModel();
            
                    int rc = dtm.getRowCount();
                    int rct = dtmo.getRowCount();
                    db.DbConnect.st.execute("alter table category_info auto_increment = "+rc);
                    db.DbConnect.st.execute("alter table spendings auto_increment = "+rct);

                    Icon successicon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/successicon.png"));
                    JOptionPane.showMessageDialog(null,"<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">Category Deleted Successfully</p></html>","Success", JOptionPane.INFORMATION_MESSAGE,successicon);
                    getEntries();
                } catch(Exception ex){
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        }
    }//GEN-LAST:event_deleteCategoryActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String category=t.getText();
            if(!category.equals("")){

                db.DbConnect.st.executeUpdate("insert into category_info(category) values('" + category + "')");
                Icon successicon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/successicon.png"));

                JOptionPane.showMessageDialog(null,"<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">Category Added Successfully</p></html>","Success", JOptionPane.INFORMATION_MESSAGE,successicon);
                getEntries();
            }else{
                Icon erroricon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/errorIcon.png"));
                JOptionPane.showMessageDialog(null, "<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">Please enter a Name</p></html>", "Warning", HEIGHT, erroricon);
            }
        }
        catch(SQLIntegrityConstraintViolationException ex){
            Icon erroricon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/errorIcon.png"));
            JOptionPane.showMessageDialog(null, "<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">Category with same name already exists</p></html>", "Warning", HEIGHT, erroricon);
        }
        catch(SQLSyntaxErrorException ex){
            Icon erroricon = new javax.swing.ImageIcon(getClass().getResource("/Extrafiles/errorIcon.png"));
            JOptionPane.showMessageDialog(null, "<html><p style=\"color:black; font-size:18px; font-family: 'calibri'\">You can't use ' or &#34; in Category Name </p></html>", "Warning", HEIGHT, erroricon);
        }
        
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

